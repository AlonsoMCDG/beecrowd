#include <stdio.h>

int main()
{
    int N, M, i, cont = 0, gols, ans = 0;

    scanf("%d %d", &N, &M);

    while (ans += cont == M, N --)
        for (cont = i = 0; i < M; i ++)
            scanf("%d", &gols), cont += gols > 0;

    printf("%d\n", ans);

    return 0;
}
